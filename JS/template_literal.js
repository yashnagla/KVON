// Template Literals :- Template literals are a feature in JavaScript that allow for multi-line strings and string interpolation, using backticks (``) instead of quotes, enabling easier string formatting.

let num1 = 7;
let num2 = 4;
let result = num1 + num2;

console.log("Addition of " + num1 + " and " + num2 + " is " + result);
console.log(`Addition of ${num1} and ${num2} is ${result}.`);

