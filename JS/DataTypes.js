// String Data Type

// String
let user = 'Yash';
console.log(user)
console.log(typeof user)

let firstName = "Yash";
let lastName = "Nagla";
console.log(firstName + lastName)
console.log(firstName + ' ' + lastName)

// Escap Character
// "\" to use double quote
let user1 = "Yash \"Nagla\"";
console.log(user1)

// "\n" is used to create a new line
let user2 = "Yash \n Nagla";
console.log(user2)

// "\b" is used to give a backspace
let user3 = "Yash \bNagla";
console.log(user3) 


// Boolean Data Type
