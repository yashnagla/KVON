// Data Types

// Number Data Type

// Number
let data = 8;
console.log(data)
console.log(typeof data)

// Float Number
let num = 7.8;
console.log(num)
console.log(typeof num)

// Numbers in Hex Code
let hex = 0xF;
console.log(hex)
console.log(typeof hex)

// Numbers in Exponential
let exp = 1.5e6;
console.log(exp)
console.log(typeof exp)

// Numbers in Number System have a ese to count zero's in the number system

let k = 100_00_00_00;
console.log(k)
console.log(typeof k)

// Infinity Data Type
let inf = 5/0;
console.log(inf)
console.log(typeof inf)

// Max Value
console.log(Number.MAX_VALUE)
console.log(Number.MAX_VALUE * 10)

// Min Value
console.log(Number.MIN_VALUE)
console.log(Number.MIN_VALUE * 100)

